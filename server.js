const respostaCache = new Map();

// --- FUN√á√ÉO PARA CHAMAR A GEMINI ---
async function chamarGeminiIA(prompt) {
    if (!geminiModel) {
        throw new Error("Modelo Gemini n√£o inicializado. Verifique a chave de API.");
    }
    try {
        const result = await geminiModel.generateContent(prompt);
        const response = await result.response;
        const text = response.text();
        return text;
    } catch (error) {
        log(`Erro em chamarGeminiIA: ${error.message}`);
        throw error;
    }
}
